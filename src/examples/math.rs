pub fn operations() {
    // addition
    let sum: i32 = 5 + 10;
    println!("{sum}");
    // subtraction
    let difference: f64 = 95.5 - 4.3;
    println!("{difference}");
    // multiplication
    let product: i32 = 4 * 30;
    println!("{product}");
    // division
    let quotient: f64 = 56.7 / 32.2;
    let floored: i32 = 2 / 3; // Results in 0
    println!("{quotient}");
    println!("{floored}");

    // modulus
    let remainder: i32 = 43 % 5;
    println!("{remainder}");
}
